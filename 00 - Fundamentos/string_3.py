# SLICING

name = "Guilherme Arthur de Carvalho"

print(name[0])  # first letter
print(name[-1])  # last letter
print(name[:9])  # : start
print(name[10:])  # start at 10 and goes to the end
print(name[10:16])
print(name[10:16:2])  # 10:16 (:2 step):
print(name[:])  # full
print(name[::-1])  # mirroring
