# Uma boa prática em tupla é adicionar um virgula no final, para o python entender que é uma tupla e não precedencia de operadores
frutas = ("laranja", "pera", "uva",)
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil,")
print(pais)
